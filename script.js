const supportedLanguages = ["en", "ar", "ru"];
const html = document.documentElement;
const metaDescription = document.querySelector('meta[name="description"]');
const ogTitle = document.querySelector('meta[property="og:title"]');
const ogDescription = document.querySelector('meta[property="og:description"]');
const ogLocale = document.querySelector('meta[property="og:locale"]');
const menuToggle = document.querySelector(".menu-toggle");
const navLinks = document.querySelector("#navLinks");
const languageButtons = document.querySelectorAll("[data-lang]");

function getInitialLanguage() {
  const params = new URLSearchParams(window.location.search);
  const fromUrl = params.get("lang");
  const fromStorage = localStorage.getItem("siteLanguage");

  if (supportedLanguages.includes(fromUrl)) return fromUrl;
  if (supportedLanguages.includes(fromStorage)) return fromStorage;
  return "en";
}

function setLanguage(lang, shouldUpdateUrl = true) {
  const dictionary = translations[lang] || translations.en;
  const isArabic = lang === "ar";

  html.lang = lang;
  html.dir = isArabic ? "rtl" : "ltr";
  document.body.dir = isArabic ? "rtl" : "ltr";
  document.title = dictionary.metaTitle;
  if (metaDescription) metaDescription.content = dictionary.metaDescription;
  if (ogTitle) ogTitle.content = dictionary.metaTitle;
  if (ogDescription) ogDescription.content = dictionary.metaDescription;
  if (ogLocale) ogLocale.content = lang === "ar" ? "ar_AR" : lang === "ru" ? "ru_RU" : "en_US";

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.getAttribute("data-i18n");
    if (dictionary[key]) node.textContent = dictionary[key];
  });

  languageButtons.forEach((button) => {
    const isActive = button.getAttribute("data-lang") === lang;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
  });

  localStorage.setItem("siteLanguage", lang);

  if (shouldUpdateUrl) {
    const url = new URL(window.location.href);
    if (lang === "en") {
      url.searchParams.delete("lang");
    } else {
      url.searchParams.set("lang", lang);
    }
    window.history.replaceState({}, "", `${url.pathname}${url.search}${url.hash}`);
  }
}

if (menuToggle && navLinks) {
  menuToggle.addEventListener("click", () => {
    const isOpen = navLinks.classList.toggle("open");
    menuToggle.setAttribute("aria-expanded", String(isOpen));
  });

  navLinks.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", () => {
      navLinks.classList.remove("open");
      menuToggle.setAttribute("aria-expanded", "false");
    });
  });
}

languageButtons.forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.getAttribute("data-lang"));
  });
});

setLanguage(getInitialLanguage(), false);
